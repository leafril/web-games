/**
 * 받는 통 prefill 튜닝 — dev 슬라이더(page.tsx)가 런타임에 mutate 한다. 단 prefill 은
 * 스폰된 물리 바디라 라이브 반영이 안 됨 → 값 변경 후 "적용(리셋)"으로 새 보드에 반영한다.
 * phaser 비의존(순수 데이터)이라 page 가 정적 import 해도 게임 번들을 끌어오지 않는다.
 *
 * 게이지 구동 하강에서 받는 통은 "연쇄 하강의 연료이자 오버플로 위험"을 동시에 한다.
 * - density: 각 칸을 실제로 채울 확률(0~1). 낮추면 같은-과일 연속 줄이 끊겨 ripple 캐스케이드가
 *   짧아지고 총 연료도 줄어 하강 게이지가 덜 찬다 — "너무 잘 차는" 문제의 1차 손잡이.
 * - fillRatio: 채움 높이(통 높이 대비). 상단은 헤드룸 — 쏟아진 더미가 착지·머지로 스스로
 *   공간을 만들기 전에 데드라인에 닿지 않게 비워 둔다. row 는 바닥(큰 과일)부터 쌓이고 위(작은
 *   과일)부터 잘리므로, 낮춰도 게이지 비싼 큰-과일 행은 남는다(연료 절감엔 density 가 더 직접적).
 * - cell: random 레이아웃 격자 간격(px). 작을수록 빽빽(밀도↑).
 * - gap: row 레이아웃 행/과일 사이 간격(px).
 * - randomEvery: N 티어마다 1번을 random(브레이커)로, 나머지는 row(전도체)로 깐다.
 *   row 통은 같은-과일 줄이라 연쇄가 레벨을 타고 관통(플런지), random 통은 연쇄가 끊겨
 *   하강이 멈춤(숨고르기) → 플런지/숨고르기 리듬 + 무한 플런지 방지. 2=교차, 3=row2+random1.
 * - emptyLow/emptyMid/emptyHigh: 하강 시 *받는 통*을 빈 통으로 만들(prefill 제거) 확률을, *현재(떠나는)
 *   통*의 채움 비율(통 높이 대비 더미 높이, 0~1)을 3구간으로 나눠 정한다 — <30%·30~60%·>=60%.
 *   적게 쌓였으면(emptyLow 낮음) prefill 잘 나와 연료=연쇄 지속, 많이 쌓였으면(emptyHigh 높음) 거의
 *   비워 공간 확보("쌓였는데 prefill=손해" 제거 + 헤드룸). → 연쇄 깊이가 보드 관리의 함수, 빈 통은
 *   circuit breaker. 셋 다 *하강 시점*에 읽혀 즉시 반영(리셋 불필요). 구간 경계(0.3/0.6)는 GameScene 상수.
 */
export const PREFILL_TUNING = {
  density: 0.4,
  fillRatio: 0.3,
  cell: 50,
  gap: 10,
  randomEvery: 1,
  emptyLow: 0.1,
  emptyMid: 0.6,
  emptyHigh: 0.95,
};
